public class Calculadora {
    int resultadoInt;
    double resultadoDouble;
    public int somar(int valorA, int valorB){
        return resultadoInt = valorA + valorB;
    }
    public int subitrair (int valorA, int valorB) {
        return resultadoInt = valorA - valorB;
    }
    public int dividir (int valorA, int valorB){
        return resultadoInt = valorA / valorB;
    }
    public double divisaoNumerosFlutuantes (double valorA, double valorB) {
        return  resultadoDouble = valorA / valorB;
    }
}
